#!/bin/bash​

# $0 holds the name of the script itself​
echo "Script name: $0"​


# $1 holds the first argument​
echo "First argument: $1"​


# $2 holds the second argument​

echo "Second argument: $2"​



# $3 holds the third argument​

echo "Third argument: $3"​


# $@ holds all the arguments starting from $1​
echo "All arguments: $@"​


# $# holds the total number of arguments​
echo "Number of arguments: $#"
